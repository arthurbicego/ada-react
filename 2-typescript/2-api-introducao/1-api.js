// Protocolos de APIs
// REST é o padrão mais comum. API RESTful segue um padrão de 6 regras do REST.
// 1. Solicitações por HTTP.
// 2. Estabelecer comunicação stateless entre cliente e servidor. Não armazena dados da requisição.
// 3. Armazena dados em cache para otimizar as interações entre cliente e servidor.
// 4. Interface uniforme e padronizada.
// 5. Sistema em camadas para impedir do cliente ver camadas de segurança.
// 6. Possibilitar código sob demanda.

// Schema: É esperado um modelo padrão da API do que é esperado no JSON

